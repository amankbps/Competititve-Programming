#include <bits/stdc++.h>
using namespace std;
#define MOD 1000000009LL;
typedef long long ll;

int main()
{
    int x, y;
    cin >> x >> y;

    cout << x << " + " << y << " = " << (x + y) << "\n";
    cout << x << " * " << y << " = " << (1LL * x * y) << "\n";
    cout << x << " - " << y << " = " << (x - y);

    return 0;
}